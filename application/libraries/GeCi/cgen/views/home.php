<p><strong>CGen</strong> adalah salah satu produk dari GeCi yang dapat digunakan untuk menghasilkan Kode Otomatis. CGen saat ini memiliki beberapa fitur untuk menghasilkan kode secara otomatis, antaralain :</p>
<ul>
	<li>Auth Generator</li>
	<li>Controller Generator</li>
	<li>CRUD Generator</li>
	<li>Model Generator</li>
</ul>
<ol>
	<li><strong>Auth Generator</strong></li>
		Auth Generator adalah penghasil kode otomatis yang menghasilkan program untuk login, logout, user akses dan lain-lain.
	<li><strong>Controller Generator</strong></li>
		Controller Generator adalah penghasil kode otomatis yang menghasilkan controller yang dapat langsung anda pakai.
	<li><strong>CRUD Generator</strong></li>
		CRUD Generator adalah fitur unggulan dari CGen dimana fitur ini dapat menghasilkan kode program lengkap untuk melakukan operasi umum yaitu Create, Update, Delete, View, dan Manage data dari database.
	<li><strong>Model Generator</strong></li>
			Model Generator adalah fitur yang sangat penting untuk melakukan akses ke database. Model juga digunakan sebagai pendamping CRUD Generator.
</ol>